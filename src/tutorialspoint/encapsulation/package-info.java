/**
 * Contains some examples about Encapsulation topic.
 *
 * <p>
 * <b>Encapsulation</b> is one of the four fundamental OOP concepts. The other three are
 * <b>inheritance</b>, <b>polymorphism</b>, and <b>abstraction</b>.
 * </p>
 *
 * <p>
 * <b>Encapsulation</b> is the technique of making the fields in a class private and providing
 * access to the fields via public methods. If a field is declared private, it cannot be accessed by
 * anyone outside the class, thereby hiding the fields within the class. For this reason,
 * encapsulation is also referred to as data hiding.
 * </p>
 *
 * <p>
 * Encapsulation can be described as a protective barrier that prevents the code and data being
 * randomly accessed by other code defined outside the class. Access to the data and code is tightly
 * controlled by an interface.
 * </p>
 *
 * <p>
 * The main benefit of encapsulation is the ability to modify our implemented code without breaking
 * the code of others who use our code. With this feature Encapsulation gives maintainability,
 * flexibility and extensibility to our code.
 * </p>
 *
 * <p>
 * Benefits of Encapsulation:
 * </p>
 *
 * <ul>
 * <li>
 * <p>
 * The fields of a class can be made read-only or write-only.
 * </p>
 * </li>
 * <li>
 * <p>
 * A class can have total control over what is stored in its fields.
 * </p>
 * </li>
 * <li>
 * <p>
 * The users of a class do not know how the class stores its data. A class can change the data type
 * of a field and users of the class do not need to change any of their code.
 * </p>
 */
